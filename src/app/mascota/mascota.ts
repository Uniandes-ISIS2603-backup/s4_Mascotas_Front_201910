export class Mascota 
{

    /**
     * id de la mascota
     */
    id:number;

    /**
     * Nombre de la mascota
     */
    nombre:string;

    /**
     * Tipo de la mascota (Perro o gato)
     */
    tipo : string;

    /**
     * Estado de la mascota
     */
    estado:string;

    /**
     * Foto de presentación de la mascota
     */
    fotos:string[];

    /**
     * Raza de la mascota
     */
    raza :string;

    /**
     * Descripción de la mascota
     */
    descripcion:string;

    /**
     * Identificador del proceso asociado a la mascota
     */
    procesoId:number;

}